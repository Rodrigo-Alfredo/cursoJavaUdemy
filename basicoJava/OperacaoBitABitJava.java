
public class OperacaoBitABitJava {

	public static void main(String[] args) {
		
		int n1 = 89;
		int n2 = 60;
		
		/* & operação E;
		 * | operação OU;
		 * ^ operação OU exclusivo, saída verdadeira somente
		 * com uma entrada verdadeira.
		 * Utilizado em programação de baixo nível: microcontrolador,
		 * arduíno, interface com rede.
		 */
		
		System.out.println(n1 & n2);
		System.out.println(n1 | n2);
		System.out.println(n1 ^ n2);

	}

}
